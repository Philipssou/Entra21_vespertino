package beans;

public class produto {

	private int quantidade;
	private int arroz, feijao, batata, repolho, maça, gtx, alface, laranja;

	//Set and Get
	
	public int getArroz() {
		return arroz;
	}

	public void setArroz(int arroz) {
		this.arroz = arroz;
	}

	public int getFeijao() {
		return feijao;
	}

	public void setFeijao(int feijao) {
		this.feijao = feijao;
	}

	public int getBatata() {
		return batata;
	}

	public void setBatata(int batata) {
		this.batata = batata;
	}

	public int getRepolho() {
		return repolho;
	}

	public void setRepolho(int repolho) {
		this.repolho = repolho;
	}

	public int getMaça() {
		return maça;
	}

	public void setMaça(int maça) {
		this.maça = maça;
	}

	public int getGtx() {
		return gtx;
	}

	public void setGtx(int gtx) {
		this.gtx = gtx;
	}

	public int getAlface() {
		return alface;
	}

	public void setAlface(int alface) {
		this.alface = alface;
	}

	public int getLaranja() {
		return laranja;
	}

	public void setLaranja(int laranja) {
		this.laranja = laranja;
	}

	public int getQuantidade() {
		return quantidade;
	}

	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}
	
}

public class Principal {

	public static void main(String[] args) {
		

		//Laço DO WHILE
		int indice = 20;
		do {
			
			System.out.println(indice);
			indice --;
		}while(indice >= 0);
		
		/*
		 	INCREMENTADORES E DECREMENTADORES
		 	++ Incrementa +1
		 	-- 	Decrementa -1
		 	+=2 Incrementa dependendo do valor informado
		 	-=3	Decrementa dependendo do valor indormado
		 	*=2 Incrementa multiplicando por um valor
		 	/=5 Decrementa divindo por um valor
		 
		 */
		
		
		
	}

}
